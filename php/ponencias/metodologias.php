<h2> Metodologías Ágiles, por M. Noguera</h2>
<p>
    El desarrollo ágil de software refiere a métodos de ingeniería del software basados en el
    desarrollo iterativo e incremental, donde los requisitos y soluciones evolucionan mediante
    la colaboración de grupos auto organizados y multidisciplinarios. Existen muchos métodos
    de desarrollo ágil; la mayoría minimiza riesgos desarrollando software en lapsos cortos.
    El software desarrollado en una unidad de tiempo es llamado una iteración, la cual debe
    durar de una a cuatro semanas. Cada iteración del ciclo de vida incluye: planificación,
    análisis de requisitos, diseño, codificación, revisión y documentación. Una iteración
    no debe agregar demasiada funcionalidad para justificar el lanzamiento del producto al
    mercado, sino que la meta es tener una «demo» (sin errores) al final de cada iteración.
    Al final de cada iteración el equipo vuelve a evaluar las prioridades del proyecto.
</p>

