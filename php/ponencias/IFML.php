<h2> IFML (Interaction Flow Modeling Language ), por M. Cabrera</h2>
<p>
El Lenguaje de Modelado para los Flujos de Interacción.
Fuertemente inspirado por WebML, actualmente representa su generalización desde el dominio Web a un dominio genérico para la representación del Front-end de cualquier tipo de aplicación (Web, Escritorio, Mobile, etc.)
 UML incluye un conjunto de notaciones gráficas para crear modelos visuales de las interacciones del usuario y el comportamiento de aplicaciones para el usuario en los sistemas de software.
La Interacción Flujo Modeling Language fue desarrollado en 2012 y 2013 bajo la dirección de WebRatio y fue inspirado por la notación WebML, así como por algunas otras experiencias en el campo de la modelización Web.
Fue adoptado por el Object Management Group (OMG) en marzo de 2013. IFML se encuentra actualmente en versión beta 2.
</p>

