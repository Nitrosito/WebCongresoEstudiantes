<h2>Procesadores de Lenguajes, por J. Revelles</h2>
<p>
    La ponencia parte de conceptos estudiados por el alumno en cursos anteriores en asignaturas relacionadas con los Lenguajes de Programación, la Teoría de Autómatas, la algoritmia, o la Arquitectura de Computadores. A partir de ahí, el alumno aprenderá a construir y utilizar los analizadores léxicos, sintácticos (Procesadores de Lenguajes I) y semánticos (Procesadores de Lenguajes II) que integran el proceso de traducción de un lenguaje formal a la serie de instrucciones manejables por un microprocesador. Al situarse entre los Lenguajes de Programación y la Arquitectura de Computadores, esta asignatura proporciona al alumno una comprensión integral del funcionamiento, uso y programación de un computador.
</p>

