<h2>Bases de Datos Multidimensionales, por E. Garví</h2>
<p>
    Son bases de datos ideadas para desarrollar aplicaciones muy concretas, como creación deCubos OLAP. Básicamente no se diferencian demasiado de las bases de datos relacionales(una tabla en una base de datos relacional podría serlo también en una base de datosmultidimensional), la diferencia está más bien a nivel conceptual; en las bases de datosmultidimensionales los campos o atributos de una tabla pueden ser de dos tipos, o bienrepresentan dimensiones de la tabla, o bien representan métricas que se desean estudiar.
</p>
<p>
    Este tipo de base de datos se aplica sobre el sistema OLAP también llamado cubo multidimensional o hipervínculo. Se compone de hechos numéricos llamados medidas que se clasifican por dimensiones. El cubo de metadatos es creado típicamente apartar de un esquema en estrella o copo de nieve y utilizando tablas de una base de datos relacional.

</p>

