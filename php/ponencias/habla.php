<h2>Procesamiento de Habla, por Z. Callejas</h2>
<p>
    La interacción "tradicional" con un ordenador se basa en el uso de la pantalla, el teclado y el ratón, y requiere que el usuario tenga unos conocimientos básicos acerca del funcionamiento del ordenador. Además, este tipo de interacción conlleva una barrera difícil de superar para usuarios potenciales que padecen determinadas discapacidades (p. e. invidentes o personas con problemas de movilidad).
</p>
<p>
    Para solventar estas limitaciones se están desarrollando y perfeccionando técnicas de Procesamiento del Habla e Interacción Multimodal con objeto de facilitar el uso del ordenador a todo tipo de usuarios. Mediante estas técnicas se posibilita el uso de diversos dispositivos (p. e. PCs, PDAs, Tablet PCs) y se reducen los conocimientos informáticos necesarios.
</p>
<p>
    La interacción multimodal permite asimismo el desarrollo de nuevas aplicaciones informáticas. En algunos casos el ordenador incluso deja de ser visible y se limita a estar activo en todo momento, en un segundo plano.
</p>

